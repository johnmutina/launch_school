let weather = 'foggy';

switch (weather) {
  case 'sunny':
    console.log("It's a beautiful day");
    break;
  case 'rainy':
    console.log('Grab your umbrella');
    break;
  case 'foggy':
    console.log("I can't even see my nose!");
    break;
  default:
    console.log("Let's stay inside");
}
